// ============================================================================
//  net/pawn_emitter.h  —  M1.4.b
//
//  Emits a Pawn ActorOpen so the client can spawn the character mesh that
//  the PC's `AcknowledgedPawn` / `Pawn` property refers to.
//
//  ── Why splice-first ───────────────────────────────────────────────────
//  The PC's 848-bit RepLayout tail (in pc_emitter.cpp) references a Pawn
//  NetGUID captured from the original replay session.  Until we can
//  natively decode that tail and substitute our own NetGUIDs (Session M5
//  property calibration), the cleanest bridging measure is to splice the
//  captured Pawn ActorOpen bytes unchanged — same pattern that got us
//  byte-identity on the PC.
//
//  Source fixture: `captured_pkt78_bunch_stream.h` (auto-generated by
//  `src/protocol/tools/extract_pkt78_bunches_raw.py`).  Contains all
//  three bunches from captured pkt#78:
//    [0] ch=85  GUIDExport         bdb=1615   — package-map exports
//    [1] ch=0   data                 bdb= 408  — control-channel chatter
//    [2] ch=114 ctrl=1 reliable=1 bdb=2963  — Pawn ActorOpen
//
//  By re-emitting these three bunches as a single concatenated packet,
//  the client receives:
//    - Exports needed to resolve the Pawn's archetype/level NetGUIDs
//    - The Pawn actor itself with its captured state
//
//  ── What we expect ────────────────────────────────────────────────────
//  Client's `AcknowledgedPawn` reference (currently pending, encoded in
//  our spliced 848-bit PC tail) resolves to the just-spawned Pawn actor
//  → character mesh renders → HUD/HP/MP populate from the Pawn's
//  component state.
//
//  ── What's still fragile ──────────────────────────────────────────────
//  - `FIRST_BUNCH_HEADER_BIT` in the extraction script was a best-guess
//    offset (127).  If the client NAKs this bunch, adjust that constant
//    and re-run the extractor.
//  - Channel 114 in our session might collide with another channel that
//    gets opened later.  If so we'll need to remap the channel field in
//    the bunch header via post-processing.
//
//  LAYER:   net / connect-orchestration
//  OWNER:   Path B
//  SESSION: M1.4.b
// ============================================================================
#pragma once

#include <string>

struct sockaddr_in;

namespace aoc { namespace net {

class IGameServerHost;

class PawnEmitter {
public:
    PawnEmitter(IGameServerHost& host, const std::string& client_key);

    /// Emit the captured pkt#78 three-bunch stream.  Returns true on
    /// successful send.  Non-fatal if it fails — the client may still
    /// render the PC without a pawn, just without a visible character
    /// mesh (same as pre-M1.4.b behaviour).
    bool emit_captured(const sockaddr_in& client_addr);

private:
    IGameServerHost& host_;
    std::string      client_key_;
};

}} // namespace aoc::net
